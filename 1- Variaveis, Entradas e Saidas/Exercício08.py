far = float(input("Informe a temperatura em graus Fahrenheit: "))

cel = (5 * (far - 32) / 9)

print(f"A temperatura informada em graus Celcius é {cel:.2f}oC")

celcius = float(input("Informe a temperatura em graus Celsius: "))

fahrenheit = (celcius * 1.8) + 32

print(f"A temperatura informada em graus Fahrenheit é {fahrenheit:.2f}oF")
